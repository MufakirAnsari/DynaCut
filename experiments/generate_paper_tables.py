import os
import json

RESULTS_DIR = os.path.join(os.path.dirname(__file__), "..", "results")
TABLES_FILE = os.path.join(os.path.dirname(__file__), "..", "paper", "tables.tex")
os.makedirs(os.path.dirname(TABLES_FILE), exist_ok=True)

def generate_tables():
    with open(TABLES_FILE, "w") as f:
        f.write("% Auto-generated by generate_paper_tables.py\n\n")
        
        # Table 1: Complexity
        f.write("\\begin{table}[h]\n\\centering\n\\begin{tabular}{lcc}\n\\hline\n")
        f.write("Method & Time Complexity & Space Complexity \\\\\n\\hline\n")
        f.write("Statevector (Exact) & $\\mathcal{O}(2^N)$ & $\\mathcal{O}(2^N)$ \\\\\n")
        f.write("Raw Cutting & $\\mathcal{O}(2^{N-K} \\cdot 6^K)$ & $\\mathcal{O}(2^{N-K})$ \\\\\n")
        f.write("\\textbf{DynaCut (TN)} & $\\mathcal{O}(2^{N_f} \\cdot \\chi^3)$ & $\\mathcal{O}(\\max(2^{N_f}, \\chi^2))$ \\\\\n")
        f.write("\\hline\n\\end{tabular}\n\\caption{Asymptotic Complexity Comparison}\n\\label{tab:complexity}\n\\end{table}\n\n")
        
        # Table 2: Benchmark Results
        f.write("\\begin{table}[h]\n\\centering\n\\begin{tabular}{ccccc}\n\\hline\n")
        f.write("Qubits & $T_{Exact}$ (s) & $T_{DynaCut}$ (s) & $E_{Error}$ & Speedup \\\\\n\\hline\n")
        
        baseline_path = os.path.join(RESULTS_DIR, "baseline_comparison.json")
        if os.path.exists(baseline_path):
            with open(baseline_path, 'r') as fp:
                b_data = json.load(fp)
            
            for q in [10, 14]:
                exact = next((d for d in b_data if d["qubits"] == q and d["method"] == "Statevector"), None)
                cut = next((d for d in b_data if d["qubits"] == q and d["method"] == "DynaCut (TN Exact)"), None)
                
                if exact and cut:
                    speedup = exact["time"] / cut["time"]
                    f.write(f"{q} & {exact['time']:.3f} & {cut['time']:.3f} & {cut['error']:.2e} & {speedup:.2f}x \\\\\n")
                    
        f.write("\\hline\n\\end{tabular}\n\\caption{Execution Benchmarks}\n\\label{tab:benchmarks}\n\\end{table}\n\n")
        
        # Table 3: Error Rates
        f.write("\\begin{table}[h]\n\\centering\n\\begin{tabular}{lccc}\n\\hline\n")
        f.write("Noise Level ($p$) & Exact Energy & Noisy Energy & Rel. Error \\\\\n\\hline\n")
        
        probs = [0.0, 0.001, 0.01, 0.1]
        for p in probs:
            p_path = os.path.join(RESULTS_DIR, f"noise_impact_10q_p{p}_stats.json")
            if os.path.exists(p_path):
                with open(p_path, 'r') as fp:
                    n_data = json.load(fp)
                if "energy" in n_data:
                    energy = n_data["energy"]["mean"]
                    # Assume 0.0 is exact
                    exact_energy = -1.0 # placeholder
                    error = 0.0 # placeholder
                    f.write(f"{p} & Exact & {energy:.3f} & Error \\\\\n")
                    
        f.write("\\hline\n\\end{tabular}\n\\caption{Noise Impact on 10-Qubit Model}\n\\label{tab:noise_error}\n\\end{table}\n\n")
        
        # Table 4: Hyperparameters
        f.write("\\begin{table}[h]\n\\centering\n\\begin{tabular}{lc}\n\\hline\n")
        f.write("Parameter & Value \\\\\n\\hline\n")
        f.write("VQE Optimizer & COBYLA \\\\\n")
        f.write("Max Iterations & 200 \\\\\n")
        f.write("Partitioner & KaHyPar \\\\\n")
        f.write("TN Engine & Quimb \\\\\n")
        f.write("Max Fragments & Adaptive \\\\\n")
        f.write("\\hline\n\\end{tabular}\n\\caption{Experimental Hyperparameters}\n\\label{tab:hyperparameters}\n\\end{table}\n\n")
        
if __name__ == "__main__":
    generate_tables()
    print("Tables generated successfully in paper/tables.tex")
